const a = 5;
const b = 2;
const operacao = "multiplicação"


if(operacao === "adição"){
    console.log(a + b)
}
else if(operacao === "subtração"){
    console.log(a - b)
}
else if(operacao === "multiplicação"){
    console.log(a * b)
}
else if(operacao === "divisão"){
    console.log(a / b)
}
else if(operacao === "módulo"){
    console.log(a % b)
}
else{
    console.log("Esta conta é impossivel")
}
